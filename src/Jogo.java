    public void verificar (javax.swing.JButton btns[], String times[]){
        
        if(times[0].equals(times[1])){
            JOptionPane.showMessageDialog(null, "Você acertou");
            btns[0].setEnabled(false);
            btns[1].setEnabled(false);
            this.acertos ++;
        }else{
            JOptionPane.showMessageDialog(null, "Você errou", "Erro", JOptionPane.ERROR_MESSAGE);
             btns[0].setText("?");
             btns[1].setText("?");
        }
        if(acertos == 8){
            JOptionPane.showMessageDialog(null, "parabens, voce finalizou o jogo");
            System.exit(0);
        }
        this.click = 0;
    }
    
    
    
    
    
